package com.revature.calc;

public class Calculator {
	/*
	 * INSTRUCTIONS:::::::
	 * Build a Calculator class with the ability to add, subtract, divide, and
	 * multiply numbers of any type (byte, short, int, long, float, double)
	 */
	/*
	 * ADDITION
	 */
	public static byte add(byte num1, byte num2) {
		byte answer = (byte) (num1 + num2);
		return answer;
	}

	public static short add(short num1, short num2) {
		short answer = (short) (num1 + num2);
		return answer;
	}

	public static int add(int num1, int num2) {
		int answer = num1 + num2;
		return answer;
	}

	public static long add(long num1, long num2) {
		long answer = num1 + num2;
		return answer;
	}

	public static float add(float num1, float num2) {
		float answer = num1 + num2;
		return answer;
	}

	public static double add(double num1, double num2) {
		double answer = num1 + num2;
		return answer;
	}
	/*
	 * SUBTRACTION!
	 */
	public static byte sub(byte num1, byte num2) {
		byte answer = (byte) (num1 - num2);
		return answer;
	}

	public static short sub(short num1, short num2) {
		short answer = (short) (num1 - num2);
		return answer;
	}

	public static int sub(int num1, int num2) {
		int answer = num1 - num2;
		return answer;
	}

	public static long sub(long num1, long num2) {
		long answer = num1 - num2;
		return answer;
	}

	public static float sub(float num1, float num2) {
		float answer = num1 - num2;
		return answer;
	}

	public static double sub(double num1, double num2) {
		double answer = num1 - num2;
		return answer;
	}
	/*
	 * MULTIPLICATION
	 */
	public static byte mul(byte num1, byte num2) {
		byte answer = (byte) (num1 * num2);
		return answer;
	}

	public static short mul(short num1, short num2) {
		short answer = (short) (num1 * num2);
		return answer;
	}

	public static int mul(int num1, int num2) {
		int answer = num1 * num2;
		return answer;
	}

	public static long mul(long num1, long num2) {
		long answer = num1 * num2;
		return answer;
	}

	public static float mul(float num1, float num2) {
		float answer = num1 * num2;
		return answer;
	}

	public static double mul(double num1, double num2) {
		double answer = num1 * num2;
		return answer;
	}
	/*
	 * DEVISION
	 */
	public static byte div(byte num1, byte num2) {
		byte answer = (byte) (num1 / num2);
		return answer;
	}

	public static short div(short num1, short num2) {
		short answer = (short) (num1 / num2);
		return answer;
	}

	public static int div(int num1, int num2) {
		int answer = num1 / num2;
		return answer;
	}

	public static long div(long num1, long num2) {
		long answer = num1 / num2;
		return answer;
	}

	public static float div(float num1, float num2) {
		float answer = num1 / num2;
		return answer;
	}

	public static double div(double num1, double num2) {
		double answer = num1 / num2;
		return answer;
	}
	
}
